# Langkah 1: Menghitung bagian pertama
pembilang1 = 200
penyebut1 = 20 - 30  # -10
hasil1 = int(pembilang1 / penyebut1)  # Mengubah ke bilangan bulat
print(f"Bagian pertama: {pembilang1} / ({penyebut1}) = {hasil1}")

# Langkah 2: Menghitung bagian kedua
pembilang2 = 100 * (21.9 ** 2)
penyebut2 = 100 + (10 / 20)
hasil2 = int(pembilang2 / penyebut2)  # Mengubah ke bilangan bulat
print(f"Bagian kedua: ({pembilang2}) / ({penyebut2}) = {hasil2}")

# Langkah 3: Menjumlahkan hasil dari kedua bagian
total_hasil = hasil1 + hasil2
print(f"Total hasil: {hasil1} + {hasil2} = {total_hasil}")